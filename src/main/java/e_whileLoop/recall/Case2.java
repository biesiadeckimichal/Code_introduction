package e_whileLoop.recall;

public class Case2 {

    public static void main(String[] args) {
        int powerOf3 = 3;
        int exponent = 4;
        int result = 1;
        for (int i = 0; i < exponent; i++) {
            result = result * powerOf3;
        }
        System.out.println(result);
    }



}
